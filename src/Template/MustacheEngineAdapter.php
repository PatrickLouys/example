<?php

namespace Example\Template;

use Mustache_Engine;

class MustacheEngineAdapter implements Engine
{
    private $engine;

    public function __construct(Mustache_Engine $engine)
    {
        $this->engine = $engine;
    }

    public function render($template, $data = [])
    {
        return $this->engine->render($template, $data);
    }
}